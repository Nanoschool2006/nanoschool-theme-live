    function createCounter(countId, endValue, speed) {
        const counterElement = document.getElementById(countId);

        let count = parseInt(counterElement.textContent);
        function updateCounter() {
            counterElement.textContent = count + "+";
        }

        // Function to increment the counter automatically
        function incrementAutomatically() {
            if (count < endValue) {
                count++;
                updateCounter();
                setTimeout(incrementAutomatically, speed);
            }
